def process_fasta_text(fasta_string, dataset_name, output_csv_filename, batch_size=32):
    print(f"\nðŸ§¬ Processing {dataset_name} (Batch Size: {batch_size})...")
    
    # io.StringIO tricks Biopython into reading a string like a file!
    fake_file = io.StringIO(fasta_string.strip())
    records = list(SeqIO.parse(fake_file, "fasta"))
    
    valid_records = [r for r in records if len(str(r.seq)) > 10]
    
    if not valid_records:
        print(f"âŒ ERROR: No valid sequences found in your {dataset_name} text block. Did you paste them correctly?")
        return
        
    sequence_ids = []
    all_embeddings = []
    
    for i in tqdm(range(0, len(valid_records), batch_size), desc=f"Extracting {dataset_name}"):
        batch_records = valid_records[i:i + batch_size]
        batch_ids = [record.id for record in batch_records]
        batch_seqs = [str(record.seq).upper() for record in batch_records]
        
        clean_batch = [" ".join(list(re.sub(r'[UZOB]', 'X', seq))) for seq in batch_seqs]
        
        inputs = tokenizer(clean_batch, padding=True, return_tensors='pt').to(device)
        with torch.no_grad():
            outputs = model(**inputs)
        
        embeddings = torch.mean(outputs.last_hidden_state, dim=1).cpu().numpy()
        sequence_ids.extend(batch_ids)
        all_embeddings.extend(embeddings)

    if all_embeddings:
        column_names = [f"BERT_{i+1}" for i in range(1024)]
        df = pd.DataFrame(all_embeddings, columns=column_names)
        df.insert(0, "Sequence_ID", sequence_ids)
        df.to_csv(output_csv_filename, index=False)
        print(f"âœ… Saved perfectly to {output_csv_filename}")


